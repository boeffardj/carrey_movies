               Man on the Moon (1999), The Majestic (2001) et Eternal Sunshine of the Spotless Mind (2004).
               Ses performances lui valent deux Golden Globes.
               
               Il se concentre par la suite essentiellement sur des productions destinées à la jeunesse : Les Désastreuses Aventures des orphelins Baudelaire (2004), Le Drôle de Noël de Scrooge (2009), Monsieur Popper et ses pingouins (2011), L'Incroyable Burt Wonderstone (2013). 
